export const products: Product[] = [...roupas, ...relogios, ...acessorios]

export function getProductBySlug(slug: string): Product | undefined {
  return products.find(p => p.slug === slug)
}

export function getProductsByCategory(category: 'roupas' | 'relogios' | 'acessorios'): Product[] {
  const typeMap = { roupas: 'APPAREL', relogios: 'WATCH', acessorios: 'ACCESSORY' } as const
  return products.filter(p => p.type === typeMap[category])
}

export function getFeaturedProducts(limit = 8): Product[] {
  return products.filter(p => p.featured).slice(0, limit)
}

export function getRelatedProducts(product: Product, limit = 4): Product[] {
  return products
    .filter(p => p.id !== product.id && p.type === product.type)
    .slice(0, limit)
}
